    public String getIdentityName() {
        Identifikace1 identity = DataBase.getIdentifikace().getIdentity();
        if (identity.isLoggendIn()) {
            return identity.getUsername();
        } else {
            if (ReloadDatabaseModel.isConnectionValid()) {
                return "Nepřihlášen";
            }
        }
    return null;
    }

	
