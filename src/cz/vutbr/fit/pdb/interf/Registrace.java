    private void LogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInActionPerformed
String username = Username.getText();
        String password = new String(Password.getPassword());

        try {
            DataBase.getIdentifikace().login(username, password);
            reset_database.setEnabled(true);
        } catch (InvalidCredentialsException ex) {
            JOptionPane.showMessageDialog(getParent(), ex.getMessage(), "Přihlášení se nezdařilo", JOptionPane.ERROR_MESSAGE);
        }

        updateIdentityNameLabel();
        Password.setText("");
        Username.setText("");
        mainPanel.checkPanelAvailability();
    }//GEN-LAST:event_LogInActionPerformed

